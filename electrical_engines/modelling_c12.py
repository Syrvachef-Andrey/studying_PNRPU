import numpy as np

Z2 = 20
Z3 = 60
Z4 = 80

# Передаточные отношения
i1 = Z3 / Z2
i2 = Z4 / Z2

# Общее передаточное отношение
i_common = i1 * i2
print(f"Передаточные отношения:")
print(f"i1 = {i1:.2f}, i2 = {i2:.2f}")
print(f"Общее i = {i_common:.2f}")

G = 8e10
d12 = 0.024
l12 = 0.15

Ip = (np.pi * d12**4) / 32
C12 = G * Ip / l12

print(f"\nЖесткость C12:")
print(f"Диаметр вала d12 = {d12*1000:.0f} мм")
print(f"Длина l12 = {l12*1000:.0f} мм")
print(f"Момент инерции Ip = {Ip:.3e} м^4")
print(f"C12 = {C12:.2f} Н·м/рад")